export function getStoredLanguage() {
  const storedLanguage = localStorage.getItem(LANGUAGE_KEY);
  return languages.some((language) => language.enabled && language.code === storedLanguage)
    ? storedLanguage
    : "en";
}

export function storeLanguage(languageCode) {
  const nextLanguage = languages.some(
    (language) => language.enabled && language.code === languageCode
  )
    ? languageCode
    : "en";
  localStorage.setItem(LANGUAGE_KEY, nextLanguage);
  return nextLanguage;
}

export function getLanguageConfig(languageCode) {
  return languages.find((language) => language.code === languageCode) || languages[0];
}

export function translate(languageCode, key, params = {}) {
  const languageStrings = translations[languageCode] || translations.en;
  const template = languageStrings[key] || translations.en[key] || key;

  return Object.entries(params).reduce(
    (text, [paramKey, value]) => text.replaceAll(`{{${paramKey}}}`, String(value)),
    template
  );
}

export function translateWithFallback(languageCode, key, params = {}) {
  return translate(languageCode || getStoredLanguage(), key, params);
}
